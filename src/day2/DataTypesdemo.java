package day2;

public class DataTypesdemo {
	
	public static void main(String[] args) {
		
		
		//Numeric Data Type
		
		int a = 100, b=200;
		
		
		System.out.println("The Value of a is"+a);
		
		System.out.println("The Value of a is"+b);
		
		System.out.println(a+b);
		
		System.out.println("The Sum of a and b is:"+a+b);
		
		System.out.println("The Sum of a and b is:"+(a+b));
		
		byte by = 125;
		System.out.println(by);
		
		short sh = 3535;
		System.out.println(sh);
		
		long l =123456789l;      //Literal is needed  L/l
		System.out.println(l);
		
		//Decimal Number - float,double
		
		float item_price=15.5f;   //Literal is needed F/f
		System.out.println(item_price);
		
		double dbl = 1234.12121212;
		System.out.println(dbl);
		
		char grade = 'A';
		System.out.println(grade);
		
		String name = "john";
		System.out.println(name);
		
		//  char ch = 'ABC'  //invalid
		//String ch = 'ABC' //invalid
		//String ch = 'A'   //invalid
		
		String ch = "B";
		System.out.println(ch);
		
		boolean bl = false;      //allows only true or false
		System.out.println(bl);

		//boolean bl = "true";   //not valid
		//boolean bl = "false";  //not valid
		
		//String bl = true ;     //not valid
		
		String al = "true";
		System.out.println(al);
		
		
		
	}

}
