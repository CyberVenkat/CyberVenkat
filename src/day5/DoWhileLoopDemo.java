package day5;

public class DoWhileLoopDemo {

	public static void main(String[] args) {
		
		//Example := 1    Print 1 to 10
		
		int i =1;
		
		do
		{
			System.out.println(i);
			i++;	
		}
		while (i<=10);
		//Example := 2 print 10 to 1
		
		int j = 10;
		do
		{
			System.out.println(j);
			j--;
		}
		while (j>0);          //while (j>=1)
		
		
		
	
	
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
